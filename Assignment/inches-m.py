# How do you convert inches to meters?
# Inch to Meter Calculator
# To convert inches to meters, you should multiply your length value by 0.0254: m = in × 0.0254 .
# To convert meters to inches, you should multiply your length value by 39.3701: in = m × 39.3701 .

print("Welcome User")
user_inches= float(input("Enter value for inches "))
user_meters = user_inches * 0.0254
print(f"Dear User, you have converted your value of inches to meter, \n the value is {user_meters}")


